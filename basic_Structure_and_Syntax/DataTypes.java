package basic_Structure_and_Syntax;

public class DataTypes {
    public static void main(String[] args) {
        
        System.out.println("Activity#2 in Data Types ");

        byte myByte = 100; // byte (stores whole numbers from -128 to 127)
        System.out.println("My Byte: " + myByte);
        short myShort = 10000; // short (stores whole numbers from -32,768 to 32,767)
        System.out.println("My Short: " + myShort);
        int myInt = 100000; // int (stores whole numbers from -2,147,483,648 to 2,147,483,647)
        System.out.println("My Int: " + myInt);
        long myLong = 1234567891L; // long (stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807)
        System.out.println("My Long: " + myLong);
        float myFloat = 3.14f; // float (stores fractional numbers, sufficient for storing 6 to 7 decimal digits)
        System.out.println("My Float: " + myFloat);
        double myDouble = 3.14159; // double (stores fractional numbers, sufficient for storing 15 decimal digits)
        System.out.println("My Double: " + myDouble);
        float MyScientificFloat = 1.23e4f; // scientific notation for float
        System.out.println("My Scientific Float: " + MyScientificFloat);
        double MyScientificDouble = 1.23e-4; // scientific notation for double
        System.out.println("My Scientific Double: " + MyScientificDouble);
        boolean myBoolean = true; // boolean (stores true or false)
        System.out.println("My Boolean: " + myBoolean);
        char myChar = 'A'; // char (stores a single character)
        System.out.println("Char Value: " + myChar);
        char myVar1 = 35, myVar2 = 36, myVar3 = 37; // declaring multiple char variables in a single line
        System.out.println(myVar1);
        System.out.println(myVar2); 
        System.out.println(myVar3); 
        String myNull;
        myNull = null; // assigning null value to a String variable
        System.out.println("My Null String: " + myNull);
        var myVarNum = 5;
        var myVarDouble = 9.98;
        var myVarChar = 'D';
        var myVarBoolean = true;
        var myVarString = "Hello";
        System.out.println(myVarNum);
        System.out.println(myVarDouble);
        System.out.println(myVarChar);
        System.out.println(myVarBoolean);
        System.out.println(myVarString);
             }
         }
